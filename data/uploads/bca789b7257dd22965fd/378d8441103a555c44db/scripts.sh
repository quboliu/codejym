#!/bin/bash
#
# grep 常用命令速查表（示例脚本）
# 所有命令均以注释形式说明功能

###############################################
# 基本匹配：在文件中搜索关键词
###############################################
# grep "keyword" file.txt

###############################################
# 忽略大小写搜索
###############################################
# grep -i "keyword" file.txt

###############################################
# 递归搜索目录中的所有文件
###############################################
# grep -r "keyword" /path/to/dir

###############################################
# 显示行号
###############################################
# grep -n "keyword" file.txt

###############################################
# 只显示文件名（仅显示包含匹配内容的文件）
###############################################
# grep -l "keyword" *.log

###############################################
# 只显示不匹配的行
###############################################
# grep -v "keyword" file.txt

###############################################
# 统计匹配到的行数
###############################################
# grep -c "keyword" file.txt

###############################################
# 启用高亮显示（一般默认开启）
###############################################
# grep --color=auto "keyword" file.txt

###############################################
# 使用正则表达式搜索（支持 | 等扩展特性）
###############################################
# grep -E "key1|key2" file.txt
# 或 egrep "key1|key2" file.txt

###############################################
# 显示匹配行前 N 行
###############################################
# grep -B 3 "keyword" file.txt   # before

###############################################
# 显示匹配行后 N 行
###############################################
# grep -A 3 "keyword" file.txt   # after

###############################################
# 显示匹配行前后各 N 行
###############################################
# grep -C 3 "keyword" file.txt   # context

###############################################
# 仅显示匹配的字段（不显示整行）
###############################################
# grep -o "pattern" file.txt

###############################################
# 整词匹配（例如 word 不会匹配 sword）
###############################################
# grep -w "word" file.txt

###############################################
# 将二进制文件作为文本处理（分析日志或 dump 时常用）
###############################################
# grep -a "keyword" binaryfile

###############################################
# 显示匹配在文件中的 byte 偏移量
###############################################
# grep -b "keyword" file.txt

###############################################
# 搜索多个文件并显示文件名
###############################################
# grep -H "keyword" *.log

###############################################
# 排除某些文件类型
###############################################
# grep -r --exclude="*.png" "keyword" .

###############################################
# 从管道输入中查找匹配（常用于日志分析）
###############################################
# tail -n 1000 app.log | grep "ERROR"

###############################################
# 匹配开头 ^
###############################################
# grep "^start" file.txt

###############################################
# 匹配结尾 $
###############################################
# grep "end$" file.txt

###############################################
# 匹配整行完全相等
###############################################
# grep -x "full line content" file.txt
